# #            Değişkenler
# #
# # Değişken, programlama dillerinde verileri saklamak için kullanılan bir isimdir.
# # Bir değişken, hafızada bir yer işaret eder ve bu yere bir değer atanabilir.
# # Bu sayede, program içinde bu değerleri tekrar tekrar kullanabiliriz
# # ve kodumuzu daha okunabilir, düzenli ve yeniden kullanılabilir hale getirebiliriz.
# #
# # Örneğin:
# # x = 10
# # Burada "x" bir değişkendir ve 10 değeriyle başlatılmıştır.
# # Değişkenlerin değerleri program çalışırken değiştirilebilir,
# # bu da programlara dinamiklik katar.

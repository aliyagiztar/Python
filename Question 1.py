# Start the program
print("Başla")

# Input the first number (a)
a = float(input("Birinci sayıyı giriniz (a): "))

# Input the second number (b)
b = float(input("İkinci sayıyı giriniz (b): "))

# Compare the two numbers
if a > b:
    # If a is greater than b
    print("Birinci sayı büyüktür")
elif b > a:
    # If b is greater than a
    print("İkinci sayı büyüktür")
else:
    # If neither is greater, they are equal
    print("Sayılar eşittir")

# End the program
print("Bitir")
